package com.bridgelabz;



public class LargestOfThree {

	public static void main(String[] args) {
		int num[]= {5,2,6};
		int max=0;
		for(int i=0;i<3;i++) {
			if(num[i]>max)
				max=num[i];
		System.out.println(num[i]);	
		}
		System.out.println("maximum number of array is :  "+max);
		
			 
		

	}

}
